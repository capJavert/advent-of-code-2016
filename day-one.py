def calc_distance(vectors):
    orientation = 0
    distance = 0

    for vector in vectors:
        if vector[0] == "L":
            orientation -= 90
        else:
            orientation += 90

        orientation %= 360

        if orientation == 0 or orientation == 90:
            distance += int(vector[1:4])
        else:
            distance -= int(vector[1:4])

    return distance


